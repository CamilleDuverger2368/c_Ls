#include "libft.h"
#include <unistd.h>

char	*ft_strcpy(char *dest, char const *src)
{
	size_t	length;
	size_t	index;

	length = ft_strlen(src);
	index = -1;
	while (++index <= length && src[index] != '\0')
		*(dest + index) = *(src + index)
			;
	dest[index] = '\0';
	return (dest);
}
